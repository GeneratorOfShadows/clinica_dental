package Modelos;
public class Recepcion {
    private int id_rcp;
    private String usuario_rcp;
    private String contraseña_rcp;
    private String nombres_rcp;
    private String apellidos_rcp;
    private String sede_rcp;
    private String celular_rcp;
    private String correo_rcp;
    private String fecha_rcp;
    private int id_tipo_rcp;
    //getter y setter
    public int getId_rcp() {return id_rcp;}
    public void setId_rcp(int id_rcp) {this.id_rcp = id_rcp;}
    public String getUsuario_rcp() {return usuario_rcp;}
    public void setUsuario_rcp(String usuario_rcp) {this.usuario_rcp = usuario_rcp;}
    public String getContraseña_rcp() {return contraseña_rcp;}
    public void setContraseña_rcp(String contraseña_rcp) {this.contraseña_rcp = contraseña_rcp;}
    public String getNombres_rcp() {return nombres_rcp;}
    public void setNombres_rcp(String nombres_rcp) {this.nombres_rcp = nombres_rcp;}
    public String getApellidos_rcp() {return apellidos_rcp;}
    public void setApellidos_rcp(String apellidos_rcp) {this.apellidos_rcp = apellidos_rcp;}
    public String getSede_rcp() {return sede_rcp;}
    public void setSede_rcp(String sede_rcp) {this.sede_rcp = sede_rcp;}
    public String getCelular_rcp() {return celular_rcp;}
    public void setCelular_rcp(String celular_rcp) {this.celular_rcp = celular_rcp;}
    public String getCorreo_rcp() {return correo_rcp;}
    public void setCorreo_rcp(String correo_rcp) {this.correo_rcp = correo_rcp;}
    public String getFecha_rcp() {return fecha_rcp;}
    public void setFecha_rcp(String fecha_rcp) {this.fecha_rcp = fecha_rcp;}
    public int getId_tipo_rcp() {return id_tipo_rcp;}
    public void setId_tipo_rcp(int id_tipo_rcp) {this.id_tipo_rcp = id_tipo_rcp;}
     
}
