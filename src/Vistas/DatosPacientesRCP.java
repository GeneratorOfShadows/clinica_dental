    public void registrar(){
        Conexion cc=new Conexion();
        Connection cn=cc.getConexion();
        try{
           String sql= "INSERT INTO historia_paciente (nombres_historiapac,apellidos_historiapac,sexo_historiapac,fechanacimiento_historiapac,dni_historiapac,raza_historiapac,lugarnac_historiapac,lugaresi_historiapac,fecha_historiapac,especialidad_historiapac) VALUES(?,?,?,?,?,?,?,?,?,?)"; 
           PreparedStatement pst=cn.prepareStatement(sql);
           pst.setString(1, jtxtNombres.getText());
           pst.setString(2, jtxtApellidos.getText());
           pst.setString(3, jtxtSexo.getText());
           pst.setString(4, ((JTextField)jFechaN.getDateEditor().getUiComponent()).getText());
           pst.setString(5, jtxtDni.getText());
           pst.setString(6, jtxtRaza.getText());
           pst.setString(7, jtxtLugarN.getText());
           pst.setString(8, jtxtLugarR.getText());
           pst.setString(9, ((JTextField)jFecha.getDateEditor().getUiComponent()).getText());
           pst.setString(10, jtxtMotivo.getText());
           pst.execute();
           JOptionPane.showMessageDialog(null, "Registro Exitoso");
        }catch (Exception e){
           JOptionPane.showMessageDialog(null, "Vaya... hay un error" + e.getMessage()); 
        }
    }
    
    public void actualizarDatos(){
        try{
            String sql=" update historia_paciente set nombres_historiapac=?,apellidos_historiapac=?,sexo_historiapac=?,fechanacimiento_historiapac=?,dni_historiapac=?,raza_historiapac=?,lugarnac_historiapac=?,lugaresi_historiapac=?,fecha_historiapac=?,especialidad_historiapac=? where id_historiapac=? ";
            int filaseleccionado=jTable1.getSelectedRow();
            String dao=(String)jTable1.getValueAt(filaseleccionado, 0);
            PreparedStatement pst=con.prepareStatement(sql);
            
            pst.setString(1, jtxtNombres.getText());
            pst.setString(2, jtxtApellidos.getText());
            pst.setString(3, jtxtSexo.getText());
            pst.setString(4, ((JTextField)jFechaN.getDateEditor().getUiComponent()).getText());
            pst.setString(5, jtxtDni.getText());
            pst.setString(6, jtxtRaza.getText());
            pst.setString(7, jtxtLugarN.getText());
            pst.setString(8, jtxtLugarR.getText());
            pst.setString(9, ((JTextField)jFecha.getDateEditor().getUiComponent()).getText());
            pst.setString(10, jtxtMotivo.getText());
            pst.setString(11, dao);
            pst.execute();
            JOptionPane.showMessageDialog(null,"Registrado Actualizado");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al Actualizar");
        }
    }
    
    public void eliminarRegistros(){
        int filaSeleccionada=jTable1.getSelectedRow();
        try{
            String sql="delete from historia_paciente where id_historiapac="+jTable1.getValueAt(filaSeleccionada, 0);
            Statement st=con.createStatement();
            int n=st.executeUpdate(sql);
            if(n>=0){
                JOptionPane.showMessageDialog(null,"Registro Eliminado");
            } 
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al eliminar Registro");
        }
    }
    
