    public void mostrarDatos(){
        String[] titulos={"Id","Nombre","Apellidos","Sexo","Fecha Naciemiento","DNI","Raza","Lugar Naciemiento","Lugar Residencia","Fecha","Motivo"};
        String[] registros=new String[11];
        
        DefaultTableModel model=new DefaultTableModel(null,titulos);
        
        String sql="select * from historia_paciente";
        
        try{
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
                registros[0]=rs.getString("id_historiapac");//id
                registros[1]=rs.getString("nombres_historiapac");
                registros[2]=rs.getString("apellidos_historiapac");
                registros[3]=rs.getString("sexo_historiapac");
                registros[4]=rs.getString("fechanacimiento_historiapac");
                registros[5]=rs.getString("dni_historiapac");
                registros[6]=rs.getString("raza_historiapac");
                registros[7]=rs.getString("lugarnac_historiapac");
                registros[8]=rs.getString("lugaresi_historiapac");
                registros[9]=rs.getString("fecha_historiapac");
                registros[10]=rs.getString("especialidad_historiapac");
                
                model.addRow(registros);
            }
            jTable1.setModel(model);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al mostrar datos");
        }
    }
